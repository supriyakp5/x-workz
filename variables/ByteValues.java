class ByteValues {
	public static void main(String[] args)
	{
		byte Height = 10, Weight = 20, Age = 30, Speed = 40, Temp = 50;
		System.out.println("Initial byte values: " + Height + ", " + Weight + ", " + Age + ", " + Speed + ", " + Temp);
        Height = 60; Weight = 70; Age = 80; Speed = 90; Temp = 100;
        System.out.println("Reinitialized byte values: " + Height + ", " + Weight + ", " + Age + ", " + Speed + ", " + Temp);
    }
}